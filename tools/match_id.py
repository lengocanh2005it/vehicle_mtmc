    for line in fin:
        line = line.strip()
        if not line:
            continue

        parts = line.split(",")

        # cột 2 dạng string → convert sang int
        col2 = int(parts[1])

        # nếu có trong mapping thì đổi
        if col2 in mapping:
            parts[1] = str(mapping[col2])

        fout.write(",".join(parts) + "\n")

